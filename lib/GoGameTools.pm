=head1 NAME

GoGameTools - Tools for the board game Go (a.k.a. Igo, Baduk, Weiqi) board game

=head1 DESCRIPTION

GoGameTools is a flexible set of tools to manipulate SGF data. SGF is used to
describe Go (a.k.a. Igo, Baduk, Weiqi) game trees.

See L<https://github.com/base64tokyo/GoGameTools> for the complete documentation.

